class Media {
	constructor(
		id,
		photographeId,
		title,
		image,
		video,
		likes,
		date,
		price,
		description
	) {
		this.id = id;
		this.photographeId = photographeId;
		this.title = title;
		this.image = image;
		this.video = video;
		this.likes = likes;
		this.date = date;
		this.price = price;
		this.description = description;
	}
}